score_per_play = {
    "A": 1,  # opponent rock
    "B": 2,  # opponent paper
    "C": 3,  # opponent scissors
    "X": 1,  # player rock
    "Y": 2,  # player paper
    "Z": 3,  # player scissors
}

score_per_result = {
    "W": 6,
    "L": 0,
    "D": 3,
}

play_to_name = {
    "A": "rock",
    "B": "paper",
    "C": "scissors",
    "X": "rock",
    "Y": "paper",
    "Z": "scissors",
}

puzzle_to_desired_outcome = {
    "X": "L",
    "Y": "D",
    "Z": "W"
}


def get_result(player_play, opponent_play):
    if player_play == opponent_play:
        return "D"
    elif (player_play == "rock" and opponent_play == "scissors") or \
            (player_play == "scissors" and opponent_play == "paper") or \
            (player_play == "paper" and opponent_play == "rock"):
        return "W"
    else:
        return "L"


def main():
    first_question()
    second_question()


def first_question():
    score = 0
    for line in puzzle_input.split('\n'):
        if not line:
            continue
        opponent_play, player_play = line.split(" ")
        result = get_result(play_to_name[player_play], play_to_name[opponent_play])
        score += score_per_result[result]
        score += score_per_play[player_play]
    print(score)


def second_question():
    score = 0
    for line in puzzle_input.split('\n'):
        if not line:
            continue
        opponent_play, desired_outcome_encoding = line.split(" ")
        desired_outcome = puzzle_to_desired_outcome[desired_outcome_encoding]
        for possible_player_play in "XYZ":
            result = get_result(play_to_name[possible_player_play], play_to_name[opponent_play])
            if result == desired_outcome:
                player_play = possible_player_play
        result = get_result(play_to_name[player_play], play_to_name[opponent_play])
        score += score_per_result[result]
        score += score_per_play[player_play]
    print(score)


if __name__ == '__main__':
    main()
